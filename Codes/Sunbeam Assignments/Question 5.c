//Write a program to display ASCII values for character constants ‘\n’, ‘\r’, ‘\t’, ‘\a’, ‘\b’

#include <stdio.h>

int main() 
{
    printf("ASCII value of '\\n' (newline): %d\n", '\n');
    printf("ASCII value of '\\r' (carriage return): %d\n", '\r');
    printf("ASCII value of '\\t' (tab): %d\n", '\t');
    printf("ASCII value of '\\a' (alert/bell): %d\n", '\a');
    printf("ASCII value of '\\b' (backspace): %d\n", '\b');

    return 0;
}
